package ch05.array08;

public class SwapEx {

	public static void main(String[] args) {
		
		// 2개 변수의 값을 바꾸려면
		int a = 10;
		int b = 20;
		System.out.println("a : "+a);
		System.out.println("b : "+b);
		int tmp = a;
		a = b;
		b = tmp;
		
		System.out.println("a : "+a);
		System.out.println("b : "+b);
		

	}

}
